/**
 * Led Matrix Buffer
 */
uchar bufferLexMatrix [MAX_BUFFER_CHARS][COLUMNS_BY_CHAR + 1];

/**
 * Led Matrix Column Counter
 */
uchar columnCounter = 0;

/**
 * Led Matrix Delay Scroll
 */
uchar delayScroll = 0;

/**
 * Led Matrix Time Scroll
 */
uchar timeScroll = 0;

/**
 * Led Matrix Buffer Pointer
 */
uchar *bufferLexMatrixPtr;

/**
 * Led Matrix Offset Buffer
 */
uchar offsetBufferLedMatrix = 0;

/**
 * Led Matrix Scroll On
 */
uchar scrollOn = 0;

/**
 * @brief Init Led Matrix
 */
void ledMatrixInit()
{
	uchar i = 0;

	// Configure Output Led Matrix Pins
	pinDigitalOutput(F1_LEDMATRIX);
	pinDigitalOutput(F2_LEDMATRIX);
	pinDigitalOutput(F3_LEDMATRIX);
	pinDigitalOutput(F4_LEDMATRIX);
	pinDigitalOutput(F5_LEDMATRIX);
	pinDigitalOutput(F6_LEDMATRIX);
	pinDigitalOutput(F7_LEDMATRIX);

	// Configure Outputs of 74HC164 controls
	pinDigitalOutput(SRCLK_LEDMATRIX);
	pinDigitalOutput(SRDAT_LEDMATRIX);

	// Init column counter
	columnCounter = 0;

	// Disable Scroll
	scrollOn = 0;

	// Point to buffer
	bufferLexMatrixPtr = (uchar *) &bufferLexMatrix;

	// Disable Rows
	pinDigitalWriteOff(F1_LEDMATRIX);
	pinDigitalWriteOff(F2_LEDMATRIX);
	pinDigitalWriteOff(F3_LEDMATRIX);
	pinDigitalWriteOff(F4_LEDMATRIX);
	pinDigitalWriteOff(F5_LEDMATRIX);
	pinDigitalWriteOff(F6_LEDMATRIX);
	pinDigitalWriteOff(F7_LEDMATRIX);

	// Clock Off
	pinDigitalWriteOff(SRCLK_LEDMATRIX);
	// Data On
	pinDigitalWriteOn(SRDAT_LEDMATRIX);

	// Off All Columns
	for(i = 0; i < MAX_COLUMNS; i++)
	{
		pinDigitalWriteOn(SRCLK_LEDMATRIX);
		pinDigitalWriteOff(SRCLK_LEDMATRIX);
	}

	// Led Matrix Buffer Clear
	ledMatrixBufferClear();

	// Set Count 2000 Cycles at 1us = 2ms
	TACCR0 = 2000;

	// Configure Timer Source SMCLK, Up Mode
	TACTL = TASSEL1 | MC0 | TACLR | TAIE;
}

/**
 * @brief Led Matrix Buffer Clear
 */
void ledMatrixBufferClear()
{
	uchar i, j = 0;

	for(i = 0; i < MAX_BUFFER_CHARS; i++)
	{
		for(j = 0; j < COLUMNS_BY_CHAR + 1; j++)
		{
			bufferLexMatrix [i][j] = 0;
		}
	}
}

/**
 * @brief Led Matrix Write Buffer
 * @param bufferPosition Buffer Position
 * @param data Data to Write
 */
void ledMatrixWriteBuffer(uchar bufferPosition, uchar data)
{
	uchar i = 0;

	if(bufferPosition <= MAX_BUFFER_CHARS)
	{
		// Copy data caracter in buffer
		for(i = 0; i < MAX_COLUMNS; i++)
		{
			bufferLexMatrix[bufferPosition - 1][i] = ledMatrixFont[data][i];
		}
	}
}

/**
 * @brief Led Matrix Update
 */
void ledMatrixUpdate()
{
	// Clear Data Row
	pinDigitalWriteOff(F1_LEDMATRIX);
	pinDigitalWriteOff(F2_LEDMATRIX);
	pinDigitalWriteOff(F3_LEDMATRIX);
	pinDigitalWriteOff(F4_LEDMATRIX);
	pinDigitalWriteOff(F5_LEDMATRIX);
	pinDigitalWriteOff(F6_LEDMATRIX);
	pinDigitalWriteOff(F7_LEDMATRIX);

	// Verify Scroll On/Off and calculate Buffer Offset
	if(scrollOn)
	{
		delayScroll++;

		if(delayScroll == timeScroll)
		{
			delayScroll = 0;

			offsetBufferLedMatrix++;

			if(offsetBufferLedMatrix > MAX_BUFFER_OFFSET)
			{
				offsetBufferLedMatrix = 0;
			}
		}
	}

	// Increment Columna and Buffer Pointer
	columnCounter++;
	bufferLexMatrixPtr++;

	// Reset Column Counter
	if(columnCounter >= MAX_COLUMNS)
	{
		columnCounter = 0;

		// Point to buffer
		bufferLexMatrixPtr = (uchar *) &bufferLexMatrix;
		bufferLexMatrixPtr += offsetBufferLedMatrix;
	}

	// Select Column
	ledMatrixSelectColumn(columnCounter);

	// Set Rows
	ledMatrixSetRows(*bufferLexMatrixPtr);
}

/**
 * @brief Select Column Led Matrix
 * @param column Select Column
 */
void ledMatrixSelectColumn(uchar column)
{
	// On Column
	pinDigitalWriteOn(SRDAT_LEDMATRIX);

	if(column == 0)
	{
		pinDigitalWriteOff(SRDAT_LEDMATRIX);
	}

	// Clock 74HC164
	pinDigitalWriteOn(SRCLK_LEDMATRIX);
	pinDigitalWriteOff(SRCLK_LEDMATRIX);
}

/**
 * @brief Set Rows Led Matrix
 * @param dataRow Data by Row
 */
void ledMatrixSetRows(uchar dataRow)
{
	// Set Data Row
	if((dataRow & BIT0) != 0)
	{
		pinDigitalWriteOn(F1_LEDMATRIX);
	}
	if((dataRow & BIT1) != 0)
	{
		pinDigitalWriteOn(F2_LEDMATRIX);
	}
	if((dataRow & BIT2) != 0)
	{
		pinDigitalWriteOn(F3_LEDMATRIX);
	}
	if((dataRow & BIT3) != 0)
	{
		pinDigitalWriteOn(F4_LEDMATRIX);
	}
	if((dataRow & BIT4) != 0)
	{
		pinDigitalWriteOn(F5_LEDMATRIX);
	}
	if((dataRow & BIT5) != 0)
	{
		pinDigitalWriteOn(F6_LEDMATRIX);
	}
	if((dataRow & BIT6) != 0)
	{
		pinDigitalWriteOn(F7_LEDMATRIX);
	}
}

/**
 * @brief Led Matrix Scroll On
 * @param setTimeScroll Set Time Scroll
 */
void ledMatrixScrollOn(uchar setTimeScroll)
{
	// Enabled Scroll
	scrollOn = 1;

	// Set Time Scroll
	timeScroll = setTimeScroll / 2;
}

/**
 * @brief Led Matrix Scroll Off
 */
void ledMatrixScrollOff()
{
	// Disabled Scroll
	scrollOn = 0;
}

/**
 * @brief Write Message into Led Matrix Buffer
 * @param column Column to Start Write
 * @param message Array Message to Send to Led Matrix
 */
void ledMatrixWriteMessage(uchar column, const uchar message [])
{
	int i = 0;

	while(message[i] != 0)
	{
		ledMatrixWriteBuffer(column, message[i]);

		// Next Character
		column++;
		i++;
	}
}
